package practice.codingbat.warm_up_1;

public class IntMax {

    /**
     * 
     * Given three int values, a b c, return the largest.
     * 
     * @param a
     * @param b
     * @param c
     * @return
     */
    public static int intMax(int a, int b, int c) {
        return Math.max(a, Math.max(b, c));

    }

}
