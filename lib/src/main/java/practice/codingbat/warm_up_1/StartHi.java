package practice.codingbat.warm_up_1;

public class StartHi {

    /**
     * 
     * 
     * Given a string, return true if the string starts with "hi" and false
     * otherwise.
     * 
     * 
     * @param str
     * @return
     */
    public static boolean startHi(String str) {

        return str.startsWith("hi");

    }

}
