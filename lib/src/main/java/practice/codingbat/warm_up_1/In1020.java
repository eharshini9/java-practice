package practice.codingbat.warm_up_1;

public class In1020 {

    /**
     * 
     * Given 2 int values, return true if either of them is in the range 10..20
     * inclusive.
     * 
     * 
     * @param a
     * @param b
     * @return
     */
    public static boolean in1020(int a, int b) {

        return (a >= 10 && a <= 20) || (b >= 10 && b <= 20);

    }

}
